class productCar {

    constructor(name, img, price, quantity, total) {

        this.name = name;
        this.img = img;
        this.price = price;
        this.quantity = quantity;
        this.total = total;
    }

    get name() {
        return this.name;
    }
    get img() {
        return this.name;
    }
    get price() {
        return price;
    }
    get quantity() {
        return this.quantity;
    }
    get total() {
        return this.total;
    }
}